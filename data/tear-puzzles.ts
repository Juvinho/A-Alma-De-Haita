/**
 * O Tear de Häita — Prova 4 picross puzzle definitions.
 * 10 puzzles: #1-3 on 8×8, #4-7 on 12×12, #8-10 on 16×16.
 * Each puzzle is defined by its solution grid (1=filled, 0=empty).
 * Row and column clues are derived programmatically.
 */

export interface TearPuzzle {
  id: string;
  nome: string;            // Image name
  descricao: string;       // Flavor text
  size: 8 | 12 | 16;
  grid: number[][];        // solution: 1=filled 0=empty
}

// ─── Clue derivation ──────────────────────────────────────────────────────────

export function deriveClues(grid: number[][]): { rows: number[][]; cols: number[][] } {
  const rows: number[][] = grid.map((row) => {
    const clues: number[] = [];
    let count = 0;
    for (const cell of row) {
      if (cell === 1) {
        count++;
      } else if (count > 0) {
        clues.push(count);
        count = 0;
      }
    }
    if (count > 0) clues.push(count);
    return clues.length > 0 ? clues : [0];
  });

  const size = grid.length;
  const cols: number[][] = [];
  for (let c = 0; c < grid[0].length; c++) {
    const clues: number[] = [];
    let count = 0;
    for (let r = 0; r < size; r++) {
      if (grid[r][c] === 1) {
        count++;
      } else if (count > 0) {
        clues.push(count);
        count = 0;
      }
    }
    if (count > 0) clues.push(count);
    cols.push(clues.length > 0 ? clues : [0]);
  }

  return { rows, cols };
}

